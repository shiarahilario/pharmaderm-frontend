/** Reactivo: cambia cuando el usuario cambia el idioma */
export function useI18n() {
  const lang = _lang

  function t(path) {
    const keys = path.split('.')
    let obj = translations[lang.value] || translations.en
    for (const k of keys) {
      if (obj == null) return path
      obj = obj[k]
    }
    return obj ?? path
  }

  function setLang(newLang) {
    if (translations[newLang]) {
      _lang.value = newLang
      try {
        localStorage.setItem(STORAGE_KEY, newLang)
      } catch {}
    }
  }

  return { t, lang, setLang }
}

/** Actualiza el idioma global (llamar desde useSettingsStore) */
export function setGlobalLang(lang) {
  if (translations[lang]) {
    _lang.value = lang
    try {
      localStorage.setItem(STORAGE_KEY, lang)
    } catch {}
  }
}

